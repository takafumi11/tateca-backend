package com.moneyme.moneymebackend.controller;

import com.moneyme.moneymebackend.dto.response.UserResponse;
import com.moneyme.moneymebackend.dto.request.UserRequest;
import com.moneyme.moneymebackend.service.UserService;
import lombok.RequiredArgsConstructor;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PatchMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestHeader;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import static com.moneyme.moneymebackend.constants.ApiConstants.HEADER_AUTHORIZATION;
import static com.moneyme.moneymebackend.constants.ApiConstants.PATH_USERS;
import static com.moneyme.moneymebackend.constants.ApiConstants.PATH_UUID;

@RequiredArgsConstructor
@RequestMapping(PATH_USERS)
@RestController
public class UserController {
    private final UserService service;

    @PostMapping
    public ResponseEntity<UserResponse> createUser(
            // TODO: Add validation if jwt is generated by auth server or not.
            @RequestHeader(HEADER_AUTHORIZATION) String token,
            @RequestBody UserRequest request
    ) {
        UserResponse response = service.createUser(token, request);
        return ResponseEntity.status(HttpStatus.CREATED).body(response);
    }

    @PatchMapping(PATH_UUID)
    public ResponseEntity<UserResponse> updateUserInfo(
            // TODO: Add validation if jwt is generated by auth server or not.
            @RequestHeader(HEADER_AUTHORIZATION) String token,
            @PathVariable String uuid,
            @RequestBody UserRequest request
    ) {
        UserResponse response = service.updateUserInfo(token, uuid, request);
        return ResponseEntity.ok(response);
    }
}
